package localization;
/**
 * Интерфейс для слушателей, которые хотят получать уведомления
 * о смене текущей локали (языка) в приложении
 */
public interface LocaleChangeListener {
    /**
     * Метод вызывается при изменении локали в приложении.
     * Реализующий класс должен обновить отображаемые тексты
     * в соответствии с новой локалью.
     */
    void onLocaleChanged();
}
